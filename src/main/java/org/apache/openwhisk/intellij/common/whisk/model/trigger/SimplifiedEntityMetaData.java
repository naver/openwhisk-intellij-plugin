package org.apache.openwhisk.intellij.common.whisk.model.trigger;

public class SimplifiedEntityMetaData {
    private String path;
    private String name;

    public SimplifiedEntityMetaData() {
    }

    public SimplifiedEntityMetaData(String path, String name) {
        this.path = path;
        this.name = name;
    }

    public String getPath() {
        return path;
    }

    public void setPath(String path) {
        this.path = path;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPkgActionName() {
        String pkg = "";
        if (path.contains("/")) {
            pkg = path.split("/")[1] + "/";
        }
        return pkg + name;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;

        SimplifiedEntityMetaData that = (SimplifiedEntityMetaData) o;

        if (path != null ? !path.equals(that.path) : that.path != null) return false;
        return name != null ? name.equals(that.name) : that.name == null;
    }

    @Override
    public int hashCode() {
        int result = path != null ? path.hashCode() : 0;
        result = 31 * result + (name != null ? name.hashCode() : 0);
        return result;
    }
}
